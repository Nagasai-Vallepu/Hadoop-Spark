import numpy as np

arr = np.array([10, 28, 15, 19, 12])
print(arr)              # displays list of values from array
print(arr.ndim)         # returns type of array - 1 dim array
print(arr.size)         # returns number of elements in array
print(arr.dtype)        # returns data type of array
print(arr.shape)        # returns shape of numpy array
print(arr.itemsize)     # returns size of object in array
